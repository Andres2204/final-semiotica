function añadirAlCarrito(objeto) {
  const objetoNuevo = JSON.parse(localStorage.getItem("carrito"));
  if (objetoNuevo == undefined) {
    objetoNuevo = objetoNuevo.append(objeto);
  } else {
    objetoNuevo;
  }

  localStorage.setItem(objetoNuevo);
}

